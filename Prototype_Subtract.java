/** Prototype_Subtract class that subtracts
    integers, floats, doubles, and longs
    and prints the difference */
public class Prototype_Subtract
{
    // main method to execute code
    public static void main(String[] args)
    {
        // subtracting two integers
        int a = 293842;
        int b = 139535;
        int subtractAB = a - b;
        System.out.println(subtractAB);

        // subtracting two floats
        float c = 88394.1835f;
        float d = 19395.2844f;
        float subtractCD = c - d;
        System.out.println(subtractCD);

        // subtracting two doubles
        double e = 639013.7654321d;
        double f = 492374.1286423d;
        double subtractEF = e - f;
        System.out.println(subtractEF);

        // subtracting two longs
        long g = 76543l;
        long h = 65432l;
        long subtractGH = g - h;
        System.out.println(subtractGH);

    } // end main
} // end class