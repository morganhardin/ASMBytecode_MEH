/** Prototype_Divide class that divides
    integers, floats, doubles, and longs
    and prints the quotient */
public class Prototype_Divide
{
    // main method to execute code
    public static void main(String[] args)
    {
        // dividing two integers
        int a = 87343;
        int b = 46125;
        int divideAB = a / b;
        System.out.println(divideAB);

        // dividing two floats 
        float c = 84.12f;
        float d = 51.87f;
        float divideCD = c / d;
        System.out.println(divideCD);

        // dividng two double
        double e = 654.13d;
        double f = 312.54d;
        double divideEF = e / f;
        System.out.println(divideEF);

        // dividing two long
        long g = 65432l;
        long h = 24579l;
        long divideGH = g / h;
        System.out.println(divideGH);
    } // end main
} // end class