/** Prototype_Multiply class that multiplies
    integers, floats, doubles, and longs
    and prints the product */
public class Prototype_Multiply
{
    // main method to execute code
    public static void main(String[] args)
    {
        // multiplying two integers
        int a = 6584;
        int b = 3916;
        int multiplyAB = a * b;
        System.out.println(multiplyAB);

        // multiplying two floats
        float c = 87.39f;
        float d = 92.17f;
        float multiplyCD = c * d;
        System.out.println(multiplyCD);

        // multiplying two doubles
        double e = 82.123d;
        double f = 23.987d;
        double multiplyEF = e * f;
        System.out.println(multiplyEF);

        // multiplying two longs
        long g = 1234l;
        long h = 5678l;
        long multiplyGH = g * h;
        System.out.println(multiplyGH);        
    } // end main
} // end class